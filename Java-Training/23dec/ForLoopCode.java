public class ForLoopCode {
    public static void main(String[] args) {
        //1
        int i=1;
        for(;i<=5;i++){
            System.out.print(i+" ");
        }
        System.out.println();

        //2
        int j;
        for(j=2;j<=4;j++){
            System.out.print(j+" ");
        }
        System.out.println();

        //3 
        for(int k=5;k>=1;k--){
            System.out.print(k+" ");
        }
    }
}
